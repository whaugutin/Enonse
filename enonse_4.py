num_1 = 50
num_2 = 2.0

add = num_1 + num_2
subs = num_1 - num_2
mul = num_1 * num_2
div = num_1 / num_2

print(str(num_1), "+", str(num_2), "=", str(add))
print(str(num_1), "-", str(num_2), "=", str(subs))
print(str(num_1), "*", str(num_2), "=", str(mul))
print(str(num_1), "/", str(num_2), "=", str(div))